def make_shirt(size, message): #function with 2 arguments
    print("\n The size of the shirt is " + size + ".") #printing the size plus a setence,1st argument.
    print("It will contain these words: " + message) #printing the message on the shirt, 2nd argument.

make_shirt('2XL', 'Code Lab Rocks!') #Call out the function with 2 argument
make_shirt(message="Sir Usman, Best Teacher!", size='small') #Call out in a different way using keyword argument.